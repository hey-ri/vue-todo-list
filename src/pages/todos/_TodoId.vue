<template>
  <h1>To-Do Page</h1>
  <div v-if="loading">Loading...</div>
  <form v-else @submit.prevent="onSave">
    <div class="row">
      <div class="col-6">
        <div class="form-group">
          <label>Subject</label>
          <input v-model="todo.subject" type="text" class="form-control" />
        </div>
      </div>
      <div class="col-6">
        <div class="form-group">
          <label>Status</label>
          <div>
            <button
              type="button"
              class="btn"
              :class="todo.completed ? 'btn-success' : 'btn-danger'"
              @click="toggleTodoStatus"
            >
              {{ todo.completed ? 'Completed' : 'InCompleted' }}
            </button>
          </div>
        </div>
      </div>
    </div>

    <button type="submit" class="btn btn-outline-danger mr-2" @click="moveToTodolistPage">cancel</button>
    <button type="submit" class="btn btn-primary" :disabled="!todoUpdated">save</button>
  </form>
  <Toast v-if="showToast" :message="toastMessage" :type="toastAlertType" />
  <div id="hey">ri</div>
</template>
<script>
import axios from 'axios';
import { ref, computed, onBeforeMount, onMounted, onBeforeUpdate, onUpdated, onBeforeUnmount, onUnmounted } from 'vue';
import { useRoute, useRouter } from 'vue-router';
import _ from 'lodash';
import Toast from '@/components/Toast.vue';

export default {
  components: {
    Toast,
  },
  setup() {
    //dom의 마운트가 되기 전에
    onBeforeMount(() => {
      console.log(document.querySelector('#hey'));
    });
    //dom의 마운트가 되었을 때
    onMounted(() => {
      console.log(document.querySelector('#hey'));
    });
    console.log('hello');
    //state가 바뀌기 전
    onBeforeUpdate(() => {
      console.log('before update');
    });
    //state가 바뀐 후
    onUpdated(() => {
      console.log('updated');
    });

    //해당 컴포넌트에 위치하면 되지 않음.
    onBeforeUnmount(() => {
      console.log('before unmount');
    });
    onUnmounted(() => {
      // 메모리 정리를 해주기 위해 사용됨
      console.log('unmounted');
    });

    const route = useRoute();
    //클릭해서 들어온 todo page의 인덱스 번호를 나타내줌
    console.log(route.params.id);
    const todo = ref(null);
    const originalTodo = ref(null);
    const loading = ref(true);
    const showToast = ref(false);
    const toastMessage = ref('');
    const toastAlertType = ref('');
    const router = useRouter();

    const todoId = route.params.id;

    const getTodo = async () => {
      //async await로 요청을 하고 받아오는 것을 비동기로 처리하고 결과를 res에 담아서 콘솔에서 확인한다. 그러고 나서 콘솔에서 확인 한 값을 가지고 또 다시 데이터에 접근 가능하게 만들 수 있다.
      try {
        const res = await axios.get(`http://localhost:3000/todos/${todoId}`);

        // console.log(res);
        todo.value = { ...res.data };
        originalTodo.value = { ...res.data };
        loading.value = false;
      } catch (err) {
        console.log(err);
        triggerToast('somthing went to wrong from server', 'danger');
      }
    };

    const todoUpdated = computed(() => {
      return !_.isEqual(todo.value, originalTodo.value);
    });

    const toggleTodoStatus = () => {
      todo.value.completed = !todo.value.completed;
    };

    const moveToTodolistPage = () => {
      router.push({
        name: 'todos',
      });
    };

    getTodo();

    const onSave = async () => {
      try {
        const res = await axios.put(`http://localhost:3000/todos/${todoId}`, {
          subject: todo.value.subject,
          completed: todo.value.completed,
        });

        console.log(res);
        originalTodo.value = { ...res.data };
        triggerToast('Successfully saved!');
      } catch (err) {
        console.log(err);
        triggerToast('Something went to wrong from server', 'danger');
      }
    };

    const triggerToast = (message, type = 'success') => {
      toastMessage.value = message;
      showToast.value = true;
      toastAlertType.value = type;

      setTimeout(() => {
        toastAlertType.value = '';
        toastMessage.value = 's';
        showToast.value = false;
      }, 2100);
    };

    return {
      todo,
      loading,
      toggleTodoStatus,
      moveToTodolistPage,
      onSave,
      todoUpdated,
      showToast,
      toastMessage,
      toastAlertType,
    };
  },
};
</script>
